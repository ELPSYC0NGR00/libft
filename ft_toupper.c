#include <stdio.h>

int	ft_toupper(int c)

{
	if (c >= 'a' && c <= 'z')
	{
		c -= 32;
	}
	return (c);
}

int	main (void)
{
	char t;
	t = "b";
	printf('%c', t);
	ft_toupper(t);
}